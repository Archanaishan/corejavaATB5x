package src.dec17;

public class escapechar {
    public static void main(String[] args) {
        System.out.println("Archana\nDevi");//newline
        System.out.println("Archana\tDevi");//tab
        System.out.println("Archana\bDevi");//backspace
        System.out.println("Archana\rDevi");//deletes the first word
    }
}
